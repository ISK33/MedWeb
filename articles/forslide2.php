Menstruation, especially symptoms of depression, fatigue, and imbalance.</br>
Pregnancy period, although in general the symptoms of multiple sclerosis improve in women during pregnancy, but they may suffer from fatigue, problems walking, and problems of the bladder and intestine, which usually may occur in any pregnant woman.</br>
